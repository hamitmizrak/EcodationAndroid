package tutorials;

//POJO: sadece nesne değişkeni ve getter setter
public class _44_PencilPOJO {

    //Nesne değişkenleri
    private Long pencilId;
    private Long pencilName;
    private Long pencilPrice;
    private Long pencilTrade;

    //Alt+Ins
    //Getter setter
    public Long getPencilId() {
        return pencilId;
    }

    public void setPencilId(Long pencilId) {
        this.pencilId = pencilId;
    }

    public Long getPencilName() {
        return pencilName;
    }

    public void setPencilName(Long pencilName) {
        this.pencilName = pencilName;
    }

    public Long getPencilPrice() {
        return pencilPrice;
    }

    public void setPencilPrice(Long pencilPrice) {
        this.pencilPrice = pencilPrice;
    }

    public Long getPencilTrade() {
        return pencilTrade;
    }

    public void setPencilTrade(Long pencilTrade) {
        this.pencilTrade = pencilTrade;
    }
}
